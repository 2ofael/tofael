.MODEL SMALL
.STACK 100H
.DATA
CR EQU 0DH
LF EQU 0AH
S1 DB 'Give X = $'
S2 DB CR,LF,'Give Y = $'
S3 DB CR,LF,'5X +5Y = $'
.CODE
MAIN PROC

 MOV AX,@DATA
 MOV DS,AX


 MOV AH,9
 LEA DX,S1
 INT 21H
 
 MOV AH,1
 INT 21H
 MOV BL,AL

 ADD BL,AL ;2X
 ADD BL,AL ;3X
 ADD BL,AL ;4X
 ADD BL,AL ;5X


 MOV AH,9
 LEA DX,S2
 INT 21H

 MOV AH,1
 INT 21H

 ADD BL,AL ;5X+Y
 ADD BL,AL ;5X+2Y
 ADD BL,AL ;5X+3Y
 ADD BL,AL ;5X+4Y
 ADD BL,AL ;5X+5Y

 MOV AH,9
 LEA DX,S3
 INT 21H

 MOV AL,BL
 MOV AH,0
 AAA

 ADD AL,48
 ADD AH,48

 MOV BX,AX

 MOV AH,2
 MOV DL,BH
 INT 21H

 MOV AH,2
 MOV DL,BL
 INT 21H


 MOV AH,4CH
 INT 21H
 MAIN ENDP
END MAIN